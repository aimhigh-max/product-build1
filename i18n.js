const i18n = {
    currentLang: localStorage.getItem('lang') || (navigator.language.startsWith('ko') ? 'ko' : 'en'),
    
    init() {
        this.applyTranslations();
        this.updateLangUI();
    },

    setLang(lang) {
        this.currentLang = lang;
        localStorage.setItem('lang', lang);
        document.documentElement.lang = lang;
        this.applyTranslations();
        this.updateLangUI();
    },

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = translations[this.currentLang][key];
            if (translation) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translation;
                } else {
                    el.innerHTML = translation;
                }
            }
        });

        // Update document title if applicable
        const pageTitleKey = document.body.getAttribute('data-page-title');
        if (pageTitleKey) {
            const baseTitle = translations[this.currentLang][pageTitleKey];
            document.title = `${baseTitle} | 🐾 AI & 🎰 Lotto`;
        }
    },

    updateLangUI() {
        const langToggle = document.getElementById('lang-toggle');
        if (langToggle) {
            langToggle.textContent = this.currentLang === 'en' ? 'KO' : 'EN';
        }
    },

    getText(key) {
        return translations[this.currentLang][key] || key;
    }
};

document.addEventListener('DOMContentLoaded', () => {
    i18n.init();
    
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            const newLang = i18n.currentLang === 'en' ? 'ko' : 'en';
            i18n.setLang(newLang);
        });
    }
});
